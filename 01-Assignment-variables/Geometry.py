# 25. Calculate surface area of a cuboid 
# Input: l = 4, 
# b = 3, 
# h = 2 
# Output: Surface Area = ? 

l = 4
b = 3
h = 2
surface_area = 2 * (l*b + b*h + l*h)
print(F" Surface area of Cuboid - {surface_area}")


